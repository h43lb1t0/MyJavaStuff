public class Adress {
    String street;
    String houseNumber;
    int zipCode;
    String city;
    String country;

    Adress(String street, String houseNumber, int zipCode, String city, String country) {
       this.street = street;
       this.houseNumber = houseNumber;
       this.zipCode = zipCode;
       this.city = city;
       this.country = country;
    }
}
